#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char *i;

	if (n > 0)
	{
		i = (unsigned char *)s;
		while (n-- > 0)
		{
			*i = 0;
			if (n)
				i++;
		}
	}
}
